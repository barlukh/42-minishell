#include "../include/libft.h"

//Checks for alphnumeric character

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'))
	{
		return (c);
	}
	return (0);
}
