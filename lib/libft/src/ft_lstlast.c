#include "../include/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp_lst;

	if (!lst)
		return (NULL);
	tmp_lst = lst;
	while (tmp_lst->next != NULL)
	{
		tmp_lst = tmp_lst->next;
	}
	return (tmp_lst);
}
