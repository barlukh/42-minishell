#include "libft.h"

void	ft_putendl_fd2(char *s1, char *s2, int fd)
{
	while (*s1)
		write(fd, s1++, 1);
	if (s2)
		while (*s2)
			write(fd, s2++, 1);
	write(fd, "\n", 1);
}
